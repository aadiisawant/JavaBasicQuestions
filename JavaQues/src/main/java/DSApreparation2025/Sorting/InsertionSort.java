package DSApreparation2025.Sorting;

public class InsertionSort {
    public static void main(String [] args){
        int arr [] = {13,46,24,52,20,9};
        for(int i=1;i<arr.length;i++){
            int j = i-1;
            while(j>=0 && arr[j]>arr[i]){
                j--;
            }
            
        }
    }
}
